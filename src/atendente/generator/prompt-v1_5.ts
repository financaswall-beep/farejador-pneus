/**
 * Prompt do Generator — v1.5.0 (Few-Shot, 2026-05-15).
 *
 * Reescrito de "manualzão de 14 regras numeradas" (v1.4.0, ~3700 tokens)
 * para arquitetura few-shot enxuta (~1700 tokens) ancorada em 10 exemplos
 * canonicos derivados de catalog15 e das baterias do Codex.
 *
 * Princípio: LLMs aprendem comportamento por padrão (imitação) muito
 * melhor que por regra abstrata. Cada exemplo cobre 1-2 dos failure modes
 * observados em catalog15-rerun, sem repetir proibições verbais.
 *
 * Failure modes cobertos pelos exemplos:
 *   - Multi-produto vira fallback (cases 490, 496, 499, 500)
 *   - Pivot/mudança congela (cases 491, 497)
 *   - Citação de turn passado bloqueada por validator (486, 487, 495, 497)
 *   - Frete coloquial gera claim inválido ("Belford Roxo" Codex)
 *   - Closing parcial sem ack ("pago pix, busco hoje" Codex)
 *
 * Migração: rodada por feature flag GENERATOR_PROMPT_FEW_SHOT_ENABLED.
 * Quando true, este builder substitui buildGeneratorMessages.
 */

import type { OpenAIMessage } from '../../shared/llm-clients/openai.js';
import type { PlannerContext } from '../planner/context-builder.js';
import type { PlannerDecisionResult } from '../planner/service.js';
import type { ToolExecutionResult } from '../executor/tool-executor.js';
import { generatorPromptVersionV15, SAFE_FALLBACK_SAY } from './schemas.js';
import { buildGeneratorContextPayload } from './prompt.js';

export function buildGeneratorMessagesFewShot(
  context: PlannerContext,
  decision: PlannerDecisionResult,
  toolResults: ToolExecutionResult[],
): OpenAIMessage[] {
  return [
    {
      role: 'system',
      content: SYSTEM_PROMPT_V1_5,
    },
    {
      role: 'user',
      content: JSON.stringify(buildGeneratorContextPayload(context, decision, toolResults, generatorPromptVersionV15)),
    },
  ];
}

